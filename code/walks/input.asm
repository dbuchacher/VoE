; ═════════════════════════════════════════════════════════════════
; THE WALK — poll → table lookup → render → loop
;
; 10 records. No stash. No bit masking. No save/restore.
; The 256-byte table makes key-up filtering implicit:
;   scancode 0x9E (key-up 'a') → table[0x9E] = 0x00 → skip_z → spin
;
; Old walk: 16 records, stash save/restore, manual bit-7 test,
;   separate AND/mask/SHL/ADD for dword table lookup.
;   Bug surface: skip offsets, stash timing, walker parity.
;
; New walk: byte table lookup = ADD + byte READ. That's it.
;   No stash, no bit test, no mask, no SHL. Two operations
;   replace eight. The rest is poll + render + loop.
; ═════════════════════════════════════════════════════════════════

global kbd_walk, kbd_walk_len

kbd_walk:

    ; ── poll: any PS/2 data? ──
    w +1,0,0,-1                                 ; port_read(0x64) → status
    db F(U8,P,P), 0x64
    w 0,0,0,+3                                  ; AND(pipeline, 1) → bit 0
    db F(P,U8,P), 1
    skip_z (.loopback - .have_data)
.have_data:

    ; ── keyboard or mouse? re-read status, check bit 5 ──
    w +1,0,0,-1                                 ; port_read(0x64) → status (same until 0x60 read)
    db F(U8,P,P), 0x64
    w 0,0,0,+3                                  ; AND(pipeline, 0x20) → bit 5 = AUX?
    db F(P,U8,P), 0x20
    skip_nz (.mouse - .kbd)
.kbd:

    ; ── keyboard path ──
    w +1,0,0,-1                                 ; port_read(0x60) → scancode
    db F(U8,P,P), 0x60
%ifdef DEBUG
    w -1,0,0,+1                                 ; echo raw scancode to debugcon
    db F(U8,P,P), 0xE9
%endif
    w -1,0,0,0                                  ; save scancode to render_pending
    db F(U32,P,P)
    dd render_pending
    w -1,0,0,-1                                 ; add(scancode_table, pipeline)
    db F(U32,P,P)
    dd scancode_table
    w +3,0,0,0                                  ; byte_read → wave byte
    db F(P,P,P)
    skip_z (.loopback - .kbd_valid)
.kbd_valid:
    w 0,+1,0,0                                  ; apply(render_char, wave byte)
    db F(U32,P,P)
    dd render_char
%ifdef DEBUG
    w -1,0,0,+1                                 ; echo '+' to debugcon
    db F(U8,U8,P), 0xE9, '+'
%endif
    skip_nz (.loopback - .kbd_done)             ; pipeline nonzero (render_char returns), always skips
.kbd_done:

    ; ── mouse path ──
    ; read byte, save to mouse_pending, run mouse walk via walk_sub
.mouse:
    w +1,0,0,-1                                 ; port_read(0x60) → mouse byte
    db F(U8,P,P), 0x60
    w -1,0,0,0                                  ; write [mouse_pending] = pipeline
    db F(U32,P,P)
    dd mouse_pending
    w 0,+1,0,0                                  ; apply(walk_sub, mouse_walk, length)
    db F(U32,U32,U32)
    dd walk_sub
    dd mouse_walk
    dd (mouse_walk_end - mouse_walk)             ; compile-time constant (same file)

.loopback:
    w -1,0,0,-1                                 ; force nonzero for loop_back
    db F(U8,U8,P), 1, 0
.lb:
    loop_back (.lb - kbd_walk)

kbd_walk_end:
kbd_walk_len: dd (kbd_walk_end - kbd_walk)


; ═════════════════════════════════════════════════════════════════
; MOUSE WALK — 3-byte packet state machine as lattice bonds
;
; Pipeline carries the incoming byte (read from mouse_pending).
; State tracked in mouse_state BSS. On packet complete, calls
; mouse_update (x86 scaffolding) via apply.
; ═════════════════════════════════════════════════════════════════

mouse_walk:

    ; read incoming byte
    w +1,0,0,0                                  ; read [mouse_pending] → pipeline
    db F(U32,P,P)
    dd mouse_pending
    w -1,0,0,0                                  ; save to stash_a
    db F(U32,P,P)
    dd stash_a

    ; read state
    w +1,0,0,0                                  ; read [mouse_state] → pipeline
    db F(U32,P,P)
    dd mouse_state

    ; state 0?
    w 0,0,0,+1                                  ; test(pipeline, 0) → 1 if state==0
    db F(P,U8,P), 0
    skip_z (.mw_not_s0 - .mw_is_s0)
.mw_is_s0:

    ; STATE 0: check sync bit 3
    w +1,0,0,0
    db F(U32,P,P)
    dd stash_a
    w 0,0,0,+3                                  ; AND(pipeline, 0x08)
    db F(P,U8,P), 0x08
    skip_z (.mw_end - .mw_s0_sync)              ; bad sync → done
.mw_s0_sync:
    w +1,0,0,0                                  ; restore byte
    db F(U32,P,P)
    dd stash_a
    w -3,0,0,0                                  ; write_byte [mouse_pkt] = pipeline
    db F(U32,P,P)
    dd mouse_pkt
    w -1,0,0,-1                                 ; add(1, 0) → pipeline = 1
    db F(U8,U8,P), 1, 0
    w -7,0,0,0                                  ; write_dword [mouse_state] = 1
    db F(U32,P,P)
    dd mouse_state
    skip_nz (.mw_end - .mw_s0_done)
.mw_s0_done:

