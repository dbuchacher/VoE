; ═════════════════════════════════════════════════════════════════
; SCANCODE → WAVE BYTE TABLE
;
; 256 bytes indexed by raw PS/2 scancode (0x00-0xFF).
; Key-up codes (0x80-0xFF) = 0x00 — filtering is free.
; Wave byte: [7:6]=T [5:4]=D [3:2]=M [1:0]=Q
;   00=0  01=+1  11=-1
;
; Letters are phonetic coordinates (T=+1 always, lowercase):
;   D = place:    +1 front/labial, 0 alveolar, -1 back/velar
;   M = openness: +1 vowel, 0 fricative, -1 stop/nasal
;   Q = voicing:  +1 voiced, -1 voiceless
;
; Shell-1 collisions = phonetically identical sounds:
;   k=c=q=x (velar stop voiceless)  b=m (labial closed voiced)
;   d=n=j (alveolar closed voiced)  l=r=z (alveolar partial)
;   a=w (front open voiced)         u=y (back open voiced)
; ═════════════════════════════════════════════════════════════════

