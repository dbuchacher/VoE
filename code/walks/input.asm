; kbd.asm — keyboard input: scancode → wave byte → render
;
; The walk is 10 records. No stash. No bit filtering. No save/restore.
;
; A 256-byte table does ALL the work: unmapped keys, key-up codes,
; and modifier keys all map to 0x00 (silence). One skip_z handles
; everything. The table is the gauge transformation between PS/2
; scancode space and phonetic coordinate space.
;
; Special keys are force wave bytes (not phonetic):
;   Space     = 0x01 = +W (signal — separator)
;   Enter     = 0x04 = +R (advance — next line)
;   Backspace = 0xC0 = -P (erase — write backward)
; These aren't arbitrary. Backspace IS -P: write backward. Enter IS
; +R: advance. Space IS +W: a signal boundary. The forces name what
; the keys DO.
;
; USB keyboards: same approach, different table. PS/2 scancodes and
; USB HID usage codes are different scancode spaces, but both are
; just indices into a 256-byte wave byte table. When we add USB,
; only the gene (hardware reader) and table change. The walk stays.

